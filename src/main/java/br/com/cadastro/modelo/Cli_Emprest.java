package br.com.cadastro.modelo;

public class Cli_Emprest {
	private String nome;
	private double vl_emprest;
	private int qtde_parc;
	private double vl_parc;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getVl_emprest() {
		return vl_emprest;
	}
	public void setVl_emprest(double vl_emprest) {
		this.vl_emprest = vl_emprest;
	}
	public int getQtde_parc() {
		return qtde_parc;
	}
	public void setQtde_parc(int qtde_parc) {
		this.qtde_parc = qtde_parc;
	}
	public double getVl_parc() {
		return vl_parc;
	}
	public void setVl_parc(double vl_parc) {
		this.vl_parc = vl_parc;
	}
	
	
}
